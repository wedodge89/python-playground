# store the value 190 in the iq variable
iq = 190

# print the value stored in the iq variable
print(iq)

# store a value equal to iq divided by 4
user_age = iq/4

# print the value stored in the user_age variable
print(user_age)

# constants
# to denote a variable as a constant, that should not be changed, use all caps
PI = 3.14

print(PI)

