# creating/binding a variable
iq = 190
print(iq)

# statements vs. expressions
iq = 100
user_age = iq / 5

# An expression is a piece of code that returns a value.
# In this example, the portion that reads "iq / 5" is an expression.

# A statement is a line of code that performs some function.
# In the example above, both "iq = 100" and "user_age = iq / 5" are statements.

# augmented assignment operator
some_value = 5
some_value = some_value + 2
print(some_value)

some_value = 5
print(some_value)
some_value += 2
print(some_value)
some_value -= 2
print(some_value)
some_value *= 2
print(some_value)
some_value /= 2

print(some_value)