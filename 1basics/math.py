# int/float

# addition
print(2+4)
print(type(2 + 4))

# subtraction
print(2 - 4)
print(type(2 - 4))

# multiplication
print(2 * 4)
print(type(2 * 4))

# division
print(2 / 4)
print(type(2 / 4))

# zero is an integer, as it has no decimal values
print(0)
print(type(0))

# the sum of an integer and a float is a float
print(20 + 1.1)
print(type(20 + 1.1))

# the sum of two floats is a float, even if the decimal value is 0
print(9.9 + 1.1)
print(type(9.9 + 1.1))

# exponent = ** = x ^ y
print(2 ** 2)
print(2 ** 3)
print(2 ** 4)
print(2 ** 5)

# floor division = // = x / y rounded down to nearest integer
print(4 // 3)
print(14 // 5)

# modulo = % = remainder of x / y
print(10 % 4)
print(101 % 10)
print(1000 % 120)

# round
print(round(3.1))
print(round(3.5))

# absolute value
print(abs(20))
print(abs(-20))

